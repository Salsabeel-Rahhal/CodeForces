import java.util.*;
public class C{
	//Simple calculator
	public static void main(String[] arges){
		Scanner scan=new Scanner(System.in);
		long x = scan.nextLong();
		long y=scan.nextLong();
		System.out.println(x+" + "+y+" = "+(x+y));
		System.out.println(x+" * "+y+" = "+(x*y));
		System.out.println(x+" - "+y+" = "+(x-y));


		}
	}